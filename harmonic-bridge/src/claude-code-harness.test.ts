import { test } from "node:test";
import assert from "node:assert/strict";
import { mkdtempSync, readFileSync, writeFileSync, existsSync, rmSync } from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import { parse as parseYaml } from "yaml";
import { applyClaudeCodeHarness } from "./claude-code-harness.js";
import { BUILT_INS } from "./steps.js";

const STUB_YML = `# harmonic-bridge agent config for "alice" — generated by 'harmonic-bridge add'.
# Edit wake_command and working_dir before relying on this agent.

harmonic_mcp_endpoint: https://app.harmonic.example/mcp
harmonic_token: file:///secrets/alice/harmonic_token
webhook_secret: file:///secrets/alice/webhook_secret

working_dir: /agents/alice

wake_command: |
  echo "wake_command not configured for $HARMONIC_BRIDGE_AGENT_NAME" >&2
  exit 1

events:
  - notifications.delivered
  - reminders.delivered
`;

function makeAgentDir(yml: string = STUB_YML): string {
  const dir = mkdtempSync(path.join(tmpdir(), "claude-harness-"));
  writeFileSync(path.join(dir, "harmonic-bridge.yml"), yml);
  return dir;
}

test("claude-code-harness: replaces the stub wake_command and writes system-prompt.md", async (t) => {
  const dir = makeAgentDir();
  t.after(() => rmSync(dir, { recursive: true, force: true }));

  const result = await applyClaudeCodeHarness({ agentDir: dir });
  assert.equal(result.updatedWakeCommand, true);
  assert.equal(result.wroteSystemPrompt, true);

  const yml = parseYaml(readFileSync(path.join(dir, "harmonic-bridge.yml"), "utf8")) as Record<string, unknown>;
  const wake = yml["wake_command"] as string;
  assert.match(wake, /claude -p/);
  assert.match(wake, /--mcp-config "\$HARMONIC_BRIDGE_AGENT_DIR\/mcp-config\.json"/);
  assert.match(wake, /--append-system-prompt @"\$HARMONIC_BRIDGE_AGENT_DIR\/system-prompt\.md"/);
  assert.match(wake, /mcp__harmonic-\$\{HARMONIC_BRIDGE_AGENT_NAME\}__execute_action/);
  assert.equal(yml["timeout_seconds"], 900);
  // Untouched fields survive.
  assert.equal(yml["harmonic_mcp_endpoint"], "https://app.harmonic.example/mcp");
  assert.deepEqual(yml["events"], ["notifications.delivered", "reminders.delivered"]);

  const prompt = readFileSync(path.join(dir, "system-prompt.md"), "utf8");
  assert.match(prompt, /fetch_page on \/whoami/);
  assert.match(prompt, /execute_action/);
  assert.doesNotMatch(prompt, /harmonic:\/\/context/, "prompt must not reference the resource URI as a fetch_page path");
});

test("claude-code-harness: leaves a customized wake_command alone", async (t) => {
  const custom = STUB_YML.replace(
    /wake_command: \|\n(  .*\n)+/,
    "wake_command: |\n  my-custom-harness --run\n",
  );
  const dir = makeAgentDir(custom);
  t.after(() => rmSync(dir, { recursive: true, force: true }));

  const result = await applyClaudeCodeHarness({ agentDir: dir });
  assert.equal(result.updatedWakeCommand, false);

  const yml = parseYaml(readFileSync(path.join(dir, "harmonic-bridge.yml"), "utf8")) as Record<string, unknown>;
  assert.match(yml["wake_command"] as string, /my-custom-harness --run/);
  assert.equal(yml["timeout_seconds"], undefined, "must not add timeout to a customized config");
});

test("claude-code-harness: does not overwrite an existing system-prompt.md", async (t) => {
  const dir = makeAgentDir();
  t.after(() => rmSync(dir, { recursive: true, force: true }));
  writeFileSync(path.join(dir, "system-prompt.md"), "MY CUSTOM PROMPT\n");

  const result = await applyClaudeCodeHarness({ agentDir: dir });
  assert.equal(result.wroteSystemPrompt, false);
  assert.equal(readFileSync(path.join(dir, "system-prompt.md"), "utf8"), "MY CUSTOM PROMPT\n");
});

test("claude-code-harness: is idempotent across two runs", async (t) => {
  const dir = makeAgentDir();
  t.after(() => rmSync(dir, { recursive: true, force: true }));

  await applyClaudeCodeHarness({ agentDir: dir });
  const afterFirst = readFileSync(path.join(dir, "harmonic-bridge.yml"), "utf8");
  const second = await applyClaudeCodeHarness({ agentDir: dir });
  assert.equal(second.updatedWakeCommand, false, "already-applied harness must not rewrite");
  assert.equal(second.wroteSystemPrompt, false);
  assert.equal(readFileSync(path.join(dir, "harmonic-bridge.yml"), "utf8"), afterFirst);
});

test("claude-code-harness: registered as a built-in step", async (t) => {
  const dir = makeAgentDir();
  t.after(() => rmSync(dir, { recursive: true, force: true }));

  const impl = BUILT_INS["claude-code-harness"];
  assert.ok(impl, "claude-code-harness must be registered in BUILT_INS");
  await impl({ agentHandle: "alice", agentDir: dir, mcpEndpoint: "https://x.example/mcp", token: "tok" });
  assert.ok(existsSync(path.join(dir, "system-prompt.md")));
});
